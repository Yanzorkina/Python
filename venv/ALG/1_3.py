x1 = int(input('Введите координату первой точки по оси Х: '))
y1 = int(input('Введите координату первой точки по оси Y: '))
x2 = int(input('Введите координату второй точки по оси Х: '))
y2 = int(input('Введите координату второй точки по оси Х: '))
k = - (y2 - y1)/(x1 - x2)
b = - ((x2 * y1)-(x1 * y2)) / (x1 - x2)
print(f'Уравнение имеет вид y = {k}x + {b}')